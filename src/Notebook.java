class Notebook {
    int RAM;
    int storage;

    int price;
    String operatingSystem;

    String model;

    Notebook(int RAM, int storage, int price, String operatingSystem, String model) {
        this.RAM = RAM;
        this.storage = storage;
        this.operatingSystem = operatingSystem;
        this.price = price;
        this.model= model;
    }
}